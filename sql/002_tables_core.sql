-- Core IMSS tables.
-- This design keeps PostgreSQL as the future local historical master.
-- Version 1 is insert-only: no period overwrite, no full_refresh and no upsert_period.

CREATE TABLE IF NOT EXISTS imss.imss_hechos_asegurados (
    periodo_informacion DATE NOT NULL,
    cve_delegacion TEXT NOT NULL DEFAULT 'no_disponible',
    cve_subdelegacion TEXT NOT NULL DEFAULT 'no_disponible',
    cve_entidad TEXT NOT NULL DEFAULT 'no_disponible',
    cve_municipio TEXT NOT NULL DEFAULT 'no_disponible',
    "tamaño_patron" TEXT NOT NULL DEFAULT 'no_disponible',
    sexo TEXT NOT NULL DEFAULT 'no_disponible',
    rango_edad TEXT NOT NULL DEFAULT 'no_disponible',
    rango_ingreso_vsm TEXT NOT NULL DEFAULT 'no_disponible',
    rango_ingreso_uma TEXT NOT NULL DEFAULT 'no_disponible',
    sector_economico_1 TEXT NOT NULL DEFAULT 'no_disponible',
    sector_economico_2 TEXT NOT NULL DEFAULT 'no_disponible',
    sector_economico_4 TEXT NOT NULL DEFAULT 'no_disponible',
    ptpd TEXT NOT NULL DEFAULT 'no_disponible',

    asegurados NUMERIC NOT NULL DEFAULT 0,
    no_trabajadores NUMERIC NOT NULL DEFAULT 0,
    ta NUMERIC NOT NULL DEFAULT 0,
    ta_sal NUMERIC NOT NULL DEFAULT 0,
    tpu NUMERIC NOT NULL DEFAULT 0,
    tpc NUMERIC NOT NULL DEFAULT 0,
    teu NUMERIC NOT NULL DEFAULT 0,
    tec NUMERIC NOT NULL DEFAULT 0,
    tpu_sal NUMERIC NOT NULL DEFAULT 0,
    tpc_sal NUMERIC NOT NULL DEFAULT 0,
    teu_sal NUMERIC NOT NULL DEFAULT 0,
    tec_sal NUMERIC NOT NULL DEFAULT 0,
    masa_sal_ta NUMERIC NOT NULL DEFAULT 0,
    masa_sal_tpu NUMERIC NOT NULL DEFAULT 0,
    masa_sal_tpc NUMERIC NOT NULL DEFAULT 0,
    masa_sal_teu NUMERIC NOT NULL DEFAULT 0,
    masa_sal_tec NUMERIC NOT NULL DEFAULT 0,
    puestos_permanentes NUMERIC NOT NULL DEFAULT 0,
    puestos_eventuales NUMERIC NOT NULL DEFAULT 0,
    puestos_urbanos NUMERIC NOT NULL DEFAULT 0,
    puestos_campo NUMERIC NOT NULL DEFAULT 0,
    masa_sal_permanentes NUMERIC NOT NULL DEFAULT 0,
    masa_sal_eventuales NUMERIC NOT NULL DEFAULT 0,
    masa_sal_urbanos NUMERIC NOT NULL DEFAULT 0,
    masa_sal_campo NUMERIC NOT NULL DEFAULT 0,
    sbc_total NUMERIC,
    sbc_permanentes NUMERIC,
    sbc_eventuales NUMERIC,
    sbc_urbanos NUMERIC,
    sbc_campo NUMERIC,

    run_id TEXT NOT NULL,
    period_fingerprint_hash TEXT,
    source_url TEXT,
    loaded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

