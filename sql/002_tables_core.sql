CREATE TABLE IF NOT EXISTS imss.imss_period_control (
    periodo_informacion DATE PRIMARY KEY,
    status TEXT NOT NULL,
    row_count BIGINT,
    period_fingerprint_hash TEXT,
    sum_asegurados NUMERIC,
    sum_no_trabajadores NUMERIC,
    sum_ta NUMERIC,
    sum_ta_sal NUMERIC,
    sum_masa_sal_ta NUMERIC,
    run_id TEXT,
    source_url TEXT,
    loaded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    error_message TEXT
);

CREATE TABLE IF NOT EXISTS imss.imss_run_manifest (
    run_id TEXT PRIMARY KEY,
    run_mode TEXT,
    started_at TIMESTAMP,
    finished_at TIMESTAMP,
    status TEXT,
    config_hash_sha256 TEXT,
    manifest_json JSONB,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
